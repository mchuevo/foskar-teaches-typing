function renderPic(filename, x, y)
  file = filesystem.open(filename, r)
  local resX = tonumber(file:readString(2))
  file:seek(cur,1)
  local resY = tonumber(file:readString(2))
  local pixelX = 1
  local pixelY = 1
  while pixelY <= resY do
    while pixelX <= resX do
      local realPixelX = pixelX + x - 1
      local realPixelY = pixelY + y - 1
      file:seek(cur,1)
      local backColor = tonumber(file:readString(8))
      component.gpu.setBackground(backColor)
      component.gpu.fill(realPixelX,realPixelY,1,1," ")
      pixelX = pixelX + 1
    end
    if pixelX >= resX then
      pixelY = pixelY + 1
      pixelX = 1
    end
  end
end
